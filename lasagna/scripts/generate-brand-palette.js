#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const BRAND_BASE = {
  1: '#EF4529',
  2: '#F6B0DB',
  3: '#02B170',
  4: '#D6FF65',
};

const LEVELS = [100, 200, 300, 400, 500, 600, 700, 800, 900, 1000];

// Tint: mix with white (levels 100-400, amount = level/1000 * some factor... simplified below)
// 100 = 10% color, 200 = 20%, 300 = 30%, 400 = 40%
// 500 = base color
// 600-1000: shade toward black
// 600 = 85% of base, 700 = 70%, 800 = 55%, 900 = 40%, 1000 = 25%
const TINT_AMOUNT = {100: 0.1, 200: 0.2, 300: 0.3, 400: 0.4};
const SHADE_FACTOR = {600: 0.85, 700: 0.70, 800: 0.55, 900: 0.40, 1000: 0.25};

function hexToRgb(hex) {
  return [
    parseInt(hex.slice(1, 3), 16),
    parseInt(hex.slice(3, 5), 16),
    parseInt(hex.slice(5, 7), 16),
  ];
}

function rgbToHex(r, g, b) {
  return '#' + [r, g, b]
    .map(v => Math.round(Math.max(0, Math.min(255, v))).toString(16).padStart(2, '0'))
    .join('');
}

function generatePalette() {
  const vars = [];

  for (const [n, hex] of Object.entries(BRAND_BASE)) {
    const [r, g, b] = hexToRgb(hex);

    for (const level of LEVELS) {
      let color;

      if (level === 500) {
        color = hex;
      } else if (TINT_AMOUNT[level] !== undefined) {
        const a = TINT_AMOUNT[level];
        color = rgbToHex(
          255 * (1 - a) + r * a,
          255 * (1 - a) + g * a,
          255 * (1 - a) + b * a,
        );
      } else {
        const f = SHADE_FACTOR[level];
        color = rgbToHex(r * f, g * f, b * f);
      }

      vars.push(`  --brand-${n}-${level}: ${color};`);
    }
  }

  return vars;
}

function main() {
  const vars = generatePalette();
  const output = [
    '// Auto-generated by scripts/generate-brand-palette.js — do not edit manually',
    ':root {',
    ...vars,
    '}',
    '',
  ].join('\n');

  const outPath = path.join(__dirname, '..', 'src', 'scss', '_brand-palette.scss');
  fs.writeFileSync(outPath, output, 'utf8');
  console.log(`Brand palette written to ${outPath} (${vars.length} variables)`);
}

if (require.main === module) {
  main();
}

module.exports = {generatePalette};
